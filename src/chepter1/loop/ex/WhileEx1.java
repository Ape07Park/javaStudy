package chepter1.loop.ex;

public class WhileEx1 {

    public static void main(String[] args) {

        int count = 1;

        while (count <= 10) {
            System.out.println(count);
            count++;
        }

        System.out.println("while 끝");

        for(int i = 1; i <=10; i++) {
            System.out.println(i);
        }

    }
}
