package variable;

public class Var7 {

    public static void main(String[] args) {

    int a = 100; // 정수
    double b = 10.5; // 실수
    boolean c = true; // 불리언 true, false
    char d = 'A';
    String e = "Hello Java"; // 문자열(참조 자료형)

        // 100, 10.5와 같이 개발자가 직접 적어넣은 값을
        // 리터럴(문자, 글자)이라고 한다.
        // 리터럴은 개발자가 직접 넣은 것이라 고정된 것이고 변화 x

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
